
// Variavel do tipo "let" é modificavel:
let variavelModificavel = "let1"
variavelModificavel = "let2"

console.log(variavelModificavel)


//Variavel do tipo "const" não pode ser modificada:
const variavelNaoModificavel = "const1"
variavelNaoModificavel = "const2"

console.log(variavelNaoModificavel)